def ensure_demos():
    script = ROOT / "scripts" / "generate-accelerator-demos.py"
    subprocess.run([sys.executable, str(script)], check=True)
    for gif_script in ["generate-code-review-gif.py", "generate-defect-triage-gif.py"]:
        path = ROOT / "scripts" / gif_script
        if path.exists():
            subprocess.run([sys.executable, str(path)], check=False)


def build():
    if not TEMPLATE.exists():
        raise SystemExit(f"Template not found: {TEMPLATE}")

    ensure_demos()

    prs = Presentation(str(TEMPLATE))
    delete_all_slides(prs)

    slide_title(prs)
    slide_agenda(prs)
    slide_executive(prs)
    slide_section(prs, "The seven AI accelerators", "Purpose · live demo preview · example · how to productionalise")
    for acc in ACCELERATORS:
        slide_accelerator(prs, acc)
    slide_section(prs, "Productionalise the suite", "Roadmap, CI integration and governance")
    slide_katalon(prs)
    slide_production(prs)
    slide_adoption(prs)
    slide_closing(prs)

    OUT.parent.mkdir(parents=True, exist_ok=True)
    prs.save(str(OUT))
    print(f"Wrote {OUT} ({len(prs.slides)} slides)")


if __name__ == "__main__":
    build()
