def slide_section(prs, title: str, subtitle: str):
    layout = get_layout(prs, *LAYOUT_TITLE_ONLY)
    slide = prs.slides.add_slide(layout)
    set_ph_text(find_placeholder(slide, 0), title, 28)
    # Some title-only layouts have extra text placeholders
    for shape in slide.shapes:
        if shape.has_text_frame and shape.name.startswith("Text Placeholder"):
            set_ph_text(shape, subtitle, 14)
            break
    return slide


