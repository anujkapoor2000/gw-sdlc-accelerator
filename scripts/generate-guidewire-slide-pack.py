def delete_all_slides(prs: Presentation) -> None:
    while len(prs.slides) > 0:
        slide_id = prs.slides._sldIdLst[0]
        rId = slide_id.rId
        prs.part.drop_rel(rId)
        del prs.slides._sldIdLst[0]


def get_layout(prs: Presentation, master_idx: int, layout_idx: int):
    return prs.slide_masters[master_idx].slide_layouts[layout_idx]


def find_placeholder(slide, idx: int):
    for shape in slide.shapes:
        if shape.is_placeholder and shape.placeholder_format.idx == idx:
            return shape
    return None


def set_ph_text(shape, text: str, font_size: int = 14) -> None:
    if shape is None:
        return
    tf = shape.text_frame
    tf.clear()
    p = tf.paragraphs[0]
    p.text = text
    p.font.size = Pt(font_size)


def set_body_bullets(shape, sections: list[tuple[str, list[str] | str]], font_size: int = 11) -> None:
    if shape is None:
        return
    tf = shape.text_frame
    tf.clear()
    first = True
    for heading, body in sections:
        if not first:
            p = tf.add_paragraph()
        else:
            p = tf.paragraphs[0]
            first = False
        p.text = heading
        p.font.size = Pt(font_size + 1)
        p.font.bold = True
        p.level = 0

        if isinstance(body, str):
            lines = body.split("\n")
        else:
            lines = body
        for line in lines:
            bp = tf.add_paragraph()
            bp.text = line
            bp.level = 1 if isinstance(body, list) else 0
            bp.font.size = Pt(font_size)


def add_demo_image(slide, module_id: str) -> None:
    png = DEMO_DIR / f"{module_id}-demo.png"
    gif = ROOT / "public" / "media" / f"{module_id}.gif"
    path = gif if gif.exists() else png
    if not path.exists():
        return
    # Right column area (matches Content Placeholder 4)
    slide.shapes.add_picture(
        str(path),
        Emu(6248400), Emu(1676400), Emu(5562600), Emu(4572000)
    )


def slide_title(prs):
    layout = get_layout(prs, *LAYOUT_TITLE_IMAGE)
    slide = prs.slides.add_slide(layout)
    for shape in slide.shapes:
        if not shape.has_text_frame:
            continue
        name = shape.name.lower()
        if "maintitle" in name or (shape.is_placeholder and shape.placeholder_format.idx == 0):
            set_ph_text(shape, "GuidewireAI\nSDLC Test Accelerators", 32)
        elif "subtitle" in name:
            set_ph_text(shape, "Seven AI modules for Guidewire InsuranceSuite — from stories to triage", 16)
        elif "text placeholder" in name and "date" not in name and "legal" not in name:
            set_ph_text(shape, "NTT DATA Guidewire Practice · July 2026", 12)
    return slide


